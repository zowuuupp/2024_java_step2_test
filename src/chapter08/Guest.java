package chapter08;

public class Guest {
	
	//필드
	String name;
	String gender;
	
	//생성
	public Guest() {
		
	}
	
	//getter & setter
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}
	
	
	
	
	
	
	
	

}
